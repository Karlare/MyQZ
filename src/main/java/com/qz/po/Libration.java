package com.qz.po;

public class Libration {
    private Integer librationId;
    private String librationName;
    private String password;

    public Integer getLibrationId() {
        return librationId;
    }

    public String getLibrationName() {
        return librationName;
    }

    public String getPassword() {
        return password;
    }

    public void setLibrationId(Integer librationId) {
        this.librationId = librationId;
    }

    public void setLibrationName(String librationName) {
        this.librationName = librationName;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}